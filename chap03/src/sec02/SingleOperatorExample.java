package sec02;

public class SingleOperatorExample {

	public static void main(String[] args) {
		int x = -100;
		int result1 = +x;
		int result2 = -x;
		System.out.println(result1);
		System.out.println(result2);
		
		byte b = 100;
		//byte result3 = -b;
		int result3 = -b;
		System.out.println(result3);

		int x1 = 1;
		++x1;
		int y1 = x1++;
		System.out.println(x1);
		System.out.println(y1);
		
		
		
	}

}
